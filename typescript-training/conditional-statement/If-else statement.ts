// If statement
// If statement will execute the block of statement or code only if the condition is true.
 let temperature : number=19;
 if(temperature>30){
    console.log("It's a hot day");
 }
 else if(temperature>=20 && temperature<=30){
    console.log("It's a pleasant day ")
 }
 else{
    console.log("It a cold day")
 }

